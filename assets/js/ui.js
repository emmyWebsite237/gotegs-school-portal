document.addEventListener('DOMContentLoaded', () => {
  // ==========================================================================
  // 1. MOBILE NAVIGATION DRAWER INTERACTIVITY
  // ==========================================================================
  const hamburger = document.querySelector('.hamburger');
  const sideDrawer = document.querySelector('.side-drawer');
  const overlay = document.querySelector('.drawer-overlay');

  if (hamburger && sideDrawer && overlay) {
    function toggleMenu() {
      const isOpen = sideDrawer.classList.contains('open');
      
      // Toggle CSS classes to slide menu and animate hamburger lines
      hamburger.classList.toggle('open');
      sideDrawer.classList.toggle('open');
      
      // Toggle accessibility states
      hamburger.setAttribute('aria-expanded', !isOpen);
      
      // Show/Hide the dark blurred background overlay
      overlay.style.display = !isOpen ? 'block' : 'none';
    }

    // Assign event listeners for mobile click states
    hamburger.addEventListener('click', toggleMenu);
    overlay.addEventListener('click', toggleMenu);
  }

  // ==========================================================================
  // 2. NAV LINK GATEKEEPER (PROTECTING INNER PORTAL LINKS)
  // ==========================================================================
  const authenticatedLinks = document.querySelectorAll('.authenticated-link');

  authenticatedLinks.forEach(link => {
    link.addEventListener('click', (e) => {
      e.preventDefault();
      
      // Look for Supabase's local storage session data
      // Supabase storage keys look similar to: sb-xxxxxxxx-auth-token
      let hasActiveSession = false;
      for (let i = 0; i < localStorage.length; i++) {
        if (localStorage.key(i).includes('-auth-token')) {
          hasActiveSession = true;
          break;
        }
      }
      
      if (!hasActiveSession) {
        alert("🔒 Access Restricted: Please sign up or log into your account to visit this school portal page.");
        window.location.href = 'signup.html';
      } else {
        // If they are logged in, send them to the page they actually clicked
        window.location.href = link.getAttribute('href');
      }
    });
  });

  // ==========================================================================
  // 3. MANUAL LINK GUARD (PROTECTING UPDATE-PASSWORD FROM DIRECT TYPING)
  // ==========================================================================
  if (window.location.pathname.includes('update-password.html')) {
    
    // Check if the URL contains the access token hash generated by clicking the email link
    const hasRecoveryToken = window.location.hash.includes('access_token=');
    
    // Check if they are logged in normally
    let hasActiveSession = false;
    for (let i = 0; i < localStorage.length; i++) {
      if (localStorage.key(i).includes('-auth-token')) {
        hasActiveSession = true;
        break;
      }
    }

    // If they don't have an email token and aren't logged in, kick them out!
    if (!hasRecoveryToken && !hasActiveSession) {
      alert("⚠️ Access Denied: This recovery page requires an authentic email link validation token. Redirecting to home...");
      window.location.href = 'index.html';
    }
  }
});
